package x3dh

import (
	"testing"

	require "github.com/stretchr/testify/require"
)

func TestCurve25519_PreFix(t *testing.T) {

	c := Curve25519{}

	// as per specification the prefix length for curve25519
	// should be 32
	require.Len(t, c.PreFix(), 32)

	require.Equal(t, []byte{
		0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
		0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
		0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
		0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
	}, c.PreFix())

}
